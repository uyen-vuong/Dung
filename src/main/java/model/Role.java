package model;

/**
 *
 * @author Admin
 */
public class Role {
    private int id;
    private String roleid, name;

    public Role() {
    }

    public Role(int id, String roleid, String name) {
        this.id = id;
        this.roleid = roleid;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getRoleid() {
        return roleid;
    }

    public void setRoleid(String roleid) {
        this.roleid = roleid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
}
