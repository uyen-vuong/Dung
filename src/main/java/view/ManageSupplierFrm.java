    private void buttonViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonViewActionPerformed
        // TODO add your handling code here:
        selectIndex = table.getSelectedRow();
        if(selectIndex == -1){
            JOptionPane.showMessageDialog(rootPane, "Hãy chọn một hàng trong bảng!");
        }
        else {
            Supplier s = listsupplier.get(selectIndex);
            SupplierDetailFrm frm = new SupplierDetailFrm(this, s, rootPaneCheckingEnabled);
            frm.setVisible(true);
        }
    }//GEN-LAST:event_buttonViewActionPerformed

