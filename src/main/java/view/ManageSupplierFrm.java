    private void showTable() {
        model.setRowCount(0);
        int i = 0;
        for(Supplier s : listsupplier){
            model.addRow(new Object[]{
                ++i, s.getFullname(), s.getEmail(), s.getPhone(), s.getAddress()
            });
        }
    }

    void updateTable() {
        listsupplier = new SupplierDAO().getAllSupplier();
        showTable();
    }
