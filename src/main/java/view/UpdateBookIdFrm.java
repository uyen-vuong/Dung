    private void buttonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSaveActionPerformed
        // TODO add your handling code here:
        if(listAbookString.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Chưa tạo mã sách!");
        }
        else {
            try {
                for(String s : listAbookString){
                    new AbookDAO().addAbook(this.book.getId(), ib.getId(), s);
                }
                JOptionPane.showMessageDialog(rootPane, "Thêm thành công mã quyển sách!");
                int importid = imp.getId();
                this.imp = new ImportDAO().getImportById(importid);
                ImportDeliveryNotNullFrm frm = new ImportDeliveryNotNullFrm(user, imp);
                this.setVisible(false);
                frm.setVisible(true);
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(rootPane, "Lỗi khi thêm mã quyển sách");
            }
        }
    }//GEN-LAST:event_buttonSaveActionPerformed

