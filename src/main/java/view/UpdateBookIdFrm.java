    private void buttonAbookidActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAbookidActionPerformed
        // TODO add your handling code here:
        listAbookString = new ArrayList<>();
        if(labelBookId.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Chưa có mã sách!");
        }
        else {
            int quantity = Integer.parseInt(labelQuantity.getText());
            int index = 1;
            if(!ib.getAbook().isEmpty()){
                String a = ib.getAbook().get(0).getAbookid();
                index = Integer.parseInt(a.substring(4));
                index++;
            }
            for(int i=0; i<quantity; i++){
                String abookid = labelBookId.getText() + String.format("%03d", index++);
                listAbookString.add(abookid);
            }
            showAbook();
        }
    }//GEN-LAST:event_buttonAbookidActionPerformed

    private void buttonIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonIdActionPerformed
        // TODO add your handling code here:
        selectIndex = tableBook.getSelectedRow();
        if(selectIndex == -1){
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa chọn sách!");
        }
        else {
            Object idObject = modelBook.getValueAt(selectIndex, 2);
            labelBookId.setText(idObject.toString());
            this.book = listbook.get(selectIndex);
        }
    }//GEN-LAST:event_buttonIdActionPerformed

    private void buttonSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSearchActionPerformed
        // TODO add your handling code here:
        String searchString = textSearch.getText();
        if(searchString.isEmpty()){                
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa nhập từ khóa!");
        }
        else {
            try {
                listbook = new BookDAO().getBookByName(searchString);
                showBook();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(rootPane, "Lỗi khi tìm kiếm!");
                Logger.getLogger(UpdateBookIdFrm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_buttonSearchActionPerformed

