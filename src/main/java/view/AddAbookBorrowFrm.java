    private void buttonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAddActionPerformed
        String abookidString = textAbookId.getText();
        Date dateDueUtil = dateDue.getDate();
        if(abookidString.isEmpty() || dateDueUtil == null){
            JOptionPane.showMessageDialog(rootPane, "Bạn nhập thiếu thông tin!");
        }
        else {
            java.sql.Date currentDate = new java.sql.Date(new Date().getTime());
            java.sql.Date dueDateSql = new java.sql.Date(dateDueUtil.getTime());
            try {
                Abook a = new AbookDAO().getAbookByAbookId(abookidString);
                if(a == null){
                    JOptionPane.showMessageDialog(rootPane, "Mã sách không đúng!");
                }
                if(!a.isStatus()){
                    JOptionPane.showMessageDialog(rootPane, "Quyển sách đã được mượn!");
                }
                else {
                    BorrowReturn br = new BorrowReturn(dueDateSql, currentDate, a, reader);
                    new BorrowReturnDAO().addBorrowReturn(br);
                    JOptionPane.showMessageDialog(rootPane, "Quyển sách đã thêm vào danh sách mượn của bạn đọc!");
                    frm.updateTable();
                    reset();
                }
            } catch (SQLException ex) {
                Logger.getLogger(AddAbookBorrowFrm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_buttonAddActionPerformed

    private void buttonCheckActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCheckActionPerformed
        // TODO add your handling code here:
        String abookidString = textAbookId.getText();
        if(abookidString.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa nhập mã sách!");
        }
        else {
            try {
                Abook a = new AbookDAO().getAbookByAbookId(abookidString);
                if(a == null){
                    JOptionPane.showMessageDialog(rootPane, "Mã sách không đúng!");
                }
                if(!a.isStatus()){
                    JOptionPane.showMessageDialog(rootPane, "Quyển sách đã được mượn!");
                }
                else {
                    labelNameBook.setText(a.getBook().getName());
                }
            } catch (SQLException ex) {
                Logger.getLogger(AddAbookBorrowFrm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_buttonCheckActionPerformed

