    private void buttonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAddActionPerformed
        // TODO add your handling code here:
        String nameGenre = textNameGenre.getText();
        if(nameGenre.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Chưa điền tên thể loại!");
        }
        else {
            try {
                int quantity = new GenreDAO().getAllGenre().size();
                new GenreDAO().addGenre(nameGenre);
                frm.updateChooseGenre(quantity);
                JOptionPane.showMessageDialog(rootPane, "Thêm thể loại thành công!");
                this.setVisible(false);
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(rootPane, "Lỗi khi thêm thể loại");
                Logger.getLogger(AddGenreImportFrm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_buttonAddActionPerformed

