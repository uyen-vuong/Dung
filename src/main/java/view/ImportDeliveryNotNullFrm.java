    private void showImportBook(Import i) {
        modelImportBook.setRowCount(0);
        labelNameUser.setText(i.getUser().getFullname());
        labelNameSupplier.setText(i.getSupplier().getFullname());
        Date importDate = new Date(i.getImportdate().getTime());
        Date deliveryDate = new Date(i.getDeliverydate().getTime());
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
        labelDateImport.setText(dateFormat.format(importDate));
        labelDateDelivery.setText(dateFormat.format(deliveryDate));
        int index=0;
        long amount = 0;
        for(ImportBook ib : i.getImportbook()){
            amount += ib.getPrice() * ib.getQuantity();
            String price_text = String.format("%,d", ib.getPrice());
            String bookidString;
            if(ib.getAbook().isEmpty()){
                bookidString = "NULL";
            }
            else {
                bookidString =ib.getAbook().get(0).getAbookid().substring(0,4);
            }
            modelImportBook.addRow(new Object[]{
                ++index, ib.getNamebook(), price_text, ib.getQuantity(), bookidString
            });
        }
        String amount_text = String.format("%,d", amount);
        labelAmount.setText(amount_text + " VND");
    }

    private void showABook(ImportBook ib) {
        modelABook.setRowCount(0);
        int index = 0;
        for(Abook a : ib.getAbook()){
            modelABook.addRow(new Object[]{
                ++index, a.getAbookid()
            });
        }
    }
